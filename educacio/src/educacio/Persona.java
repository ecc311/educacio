package educacio;

public abstract class Persona {


	
	public abstract int getIdPersona();
	public abstract  void setIdPersona(int id);
	public abstract String getNomPersona();
	
	public abstract void setNomPersona(String nom);
	public abstract int getEdatPersona();
	public  abstract void setEdatPersona(int edat);
	public abstract String getGrupPersona();

	public abstract void setGrupPersona();
	
	public abstract String getHorariPersona();

	public abstract void setHorariPersona();
	public abstract String getPasswordPersona();

	public abstract void setPasswordPersona(String password);
	
}
